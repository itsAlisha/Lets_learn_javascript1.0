//Arithmetic Operators : 
var a=1000;
var b=105;
console.log("The value of a+b is : ",a+b);
console.log("The value of a-b is : ",a-b);
console.log("The value of a*b is : ",a*b);
console.log("The value of a/b is : ",a/b);

//Assignment Operators :
var c=b;
console.log(c);
c+=3;
console.log(c);
c*=10;
console.log(c);
c/=108;
console.log(c);

//Comparison Operators :
var x=34;
var y=56;
console.log(x>=y);
console.log(x<=y);
console.log(x==y);
console.log(x>y);
console.log(x<y);

//Logical Operators :
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

console.log(!true);
